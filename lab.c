#include <stdio.h>  // Підключення бібліотеки для введення-виведення
#include <math.h>   // Підключення бібліотеки для математичних операцій

int main() {
    // Оголошення та ініціалізація змінних для координат вершин трикутника
    double x1 = 0, y1 = 0;
    double x2 = 0, y2 = 3;
    double x3 = 4, y3 = 0;

    // Обчислення довжин сторін трикутника за формулою відстані між двома точками
    double a = sqrt(pow(x2 - x1, 2) + pow(y2 - y1, 2)); // Відстань між (x1, y1) і (x2, y2)
    double b = sqrt(pow(x3 - x2, 2) + pow(y3 - y2, 2)); // Відстань між (x2, y2) і (x3, y3)
    double c = sqrt(pow(x3 - x1, 2) + pow(y3 - y1, 2)); // Відстань між (x1, y1) і (x3, y3)

    // Обчислення периметра трикутника
    double P = a + b + c;
    
    // Обчислення площі трикутника за формулою Герона
    double s = P / 2; // Напівпериметр
    double S = sqrt(s * (s - a) * (s - b) * (s - c));

    // Виведення результатів на екран
    printf("Perimeter: %.2f\n", P);
    printf("Area: %.2f\n", S);

    return 0; // Завершення програми
}